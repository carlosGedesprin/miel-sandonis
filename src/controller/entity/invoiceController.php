    public function setReg( $reg ) { $this->reg = array_merge( $this->reg, $reg );  }
    public function setId( $id ) { $this->reg['id'] = $id;  }
    public function setAccount( $account ) { $this->reg['account'] = $account;  }
    public function setDate( $date ) { $this->reg['date'] = $date;  }
    public function setNet( $net ) { $this->reg['net'] = $net;  }
    public function setVatAmount( $vatamount ) { $this->reg['vat_amount'] = $vatamount;  }
    public function setTotalToPay( $totaltopay ) { $this->reg['total_to_pay'] = $totaltopay;  }
    public function setPayed( $payed ) { $this->reg['payed'] = $payed;  }
    public function setPayment( $payment ) { $this->reg['payment'] = $payment;  }

    public function getReg() { return $this->reg; }
    public function getAll( $filter='', $extra='' ) { return $this->db->fetchAll( $this->table, '*', $filter, $extra ); }
    public function getId() { return $this->reg['id']; }
    public function getAccount() { return $this->reg['account']; }
    public function getDate() { return $this->reg['date']; }
    public function getNet() { return $this->reg['net']; }
    public function getVatAmount() { return $this->reg['vat_amount']; }
    public function getTotalToPay() { return $this->reg['total_to_pay']; }
    public function getPayed() { return $this->reg['payed']; }
    public function getPayment() { return $this->reg['payment']; }

    /**
     *
     * Change special fields after load from database
     */
    private function loadSpecialFields()
    {
//$txt = '====================== '.__METHOD__.' start ==============================================================='.PHP_EOL; fwrite($this->myfile, $txt);
        $this->reg['date'] = ( empty($this->reg['date']) )? NULL : DateTime::createFromFormat('Y-m-d', $this->reg['date'], new DateTimeZone($this->session->config['time_zone']));
//$txt = '====================== '.__METHOD__.' end ==============================================================='.PHP_EOL; fwrite($this->myfile, $txt);
    }

    /**
     *
     * Change special fields before save to database
     */
    private function setSpecialFields()
    {
//$txt = '====================== '.__METHOD__.' start ==============================================================='.PHP_EOL; fwrite($this->myfile, $txt);
        $this->reg['date'] = ( empty($this->reg['date']) ) ? NULL : $this->reg['date']->format('Y-m-d');

        $this->reg['payment'] = ( empty($this->reg['payment']) )? NULL : $this->reg['payment'];
        $this->reg['account'] = ( empty($this->reg['account']) )? NULL : $this->reg['account'];

        $this->reg['net'] = ( empty($this->reg['net']) )? '0' : $this->reg['net'];
        $this->reg['vat_amount'] = ( empty($this->reg['vat_amount']) )? '0' : $this->reg['vat_amount'];
        $this->reg['total_to_pay'] = ( empty($this->reg['total_to_pay']) )? '0' : $this->reg['total_to_pay'];
        $this->reg['payed'] = ( empty($this->reg['payed']) )? '0' : $this->reg['payed'];
//$txt = '====================== '.__METHOD__.' end ==============================================================='.PHP_EOL; fwrite($this->myfile, $txt);
    }
}