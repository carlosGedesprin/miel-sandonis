                            'date'             => NULL,
                            'author'           => NULL,
                            'picture_thumb'    => NULL,
                            'picture'          => NULL,
                            'ordinal'          => NULL,
                            'visits'           => NULL,
                            'featured'         => NULL,
                            'active'           => NULL,
                        );

